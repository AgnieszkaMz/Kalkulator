public class Dzialanie {
    private int a;
    private int b;

    public Dzialanie(int a, int b) {
        this.a = a;
        this.b = b;
    }
    public void Dodawanie() {System.out.println(a+b);}
    public void Odejmowanie() {System.out.println(a-b);}
    public void Mnozenie() {System.out.println(a*b);}
    public void Dzielenie() {System.out.println(a/b);}

}